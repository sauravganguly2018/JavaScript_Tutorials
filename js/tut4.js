// Primitive Data Type

// String
let name="saurav";
console.log("my name is",name);
console.log("Data type is",typeof name);

// Numbers
let marks=98;
console.log("Data type is",typeof marks);

// Boolean
let isworker=false;
console.log("Data type is",typeof isworker);

// Null
let nullvar=null;
console.log("Data type is",typeof nullvar);

// Undefined
let undef=undefined;
console.log("Data type is",typeof undef);

// Reference Data Types

// Arrays
let myarr=[1,2,3,7,0,4,false,"saurav"];
console.log("Data type is",typeof myarr);

// Object literals
let stmarks={
    saurav:98,
    kundan:96,
    gunjan:88,
    ranjan:76,
    'shivam pathak':99
}
console.log(stmarks);
console.log("Data type is",typeof stmarks);

// Date
let date=new Date();
console.log("Data type is",typeof date);

